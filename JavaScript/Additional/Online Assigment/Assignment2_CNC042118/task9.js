// Write a program that takes a character (i.e. string of length 1) and
// returns true if it is a vowel, false otherwise

var giveString = prompt("Enter String");

if (giveString == "a"){
    alert("True...!!")
}

else if (giveString == "e"){
    alert("True...!!")
}

else if (giveString == "i"){
    alert("True...!!")
}

else if (giveString == "o"){
    alert("True...!!")
}

else if (giveString == "u"){
    alert("True...!!")
}

else {
    alert("False..!")
}